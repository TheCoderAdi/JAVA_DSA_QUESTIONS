public class standardLibrary {
    public static void main(String[] args) {

        System.out.println(Math.sqrt(36));// Returns the square root of the value and its data type is double
        System.out.println(Math.floor(4.4));// Returns the lower and equal to value of the number
        System.out.println(Math.ceil(2.2));// Returns the higher and equal to value of the number

    }
}
